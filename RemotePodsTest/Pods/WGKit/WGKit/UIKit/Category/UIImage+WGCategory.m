    [self drawInRect:CGRectMake(0, 0, reSize.width, reSize.height)];
    UIImage *reSizeImage = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    return reSizeImage;
}

#pragma mark - 自适应size
+ (CGSize )size:(CGSize )size ThatFits:(CGSize)fitSize{
    if (size.width==0 || size.height==0) {
        return size;
    }
    if (fitSize.width==0 || fitSize.height==0) {
        return fitSize;
    }
    float wRate = size.width/fitSize.width;
    float hRate = size.height/fitSize.height;
    float rate = wRate>hRate?(fitSize.width/size.width):(fitSize.height/size.height);
    rate = MIN(1, rate);
    return CGSizeMake(size.width*rate, size.height*rate);
}
#pragma mark - UIImage 旋转
- (UIImage *)imageRotatedByRadians:(CGFloat)radians{
    UIView *rotatedViewBox = [[UIView alloc] initWithFrame:CGRectMake(0,0,self.size.width, self.size.height)];
    CGAffineTransform t = CGAffineTransformMakeRotation(radians);
    rotatedViewBox.transform = t;
    CGSize rotatedSize = rotatedViewBox.frame.size;
    rotatedViewBox = nil;
    // Create the bitmap context
    UIGraphicsBeginImageContext(rotatedSize);
    CGContextRef bitmap = UIGraphicsGetCurrentContext();
    // Move the origin to the middle of the image so we will rotate and scale around the center.
    CGContextTranslateCTM(bitmap, rotatedSize.width/2, rotatedSize.height/2);
    //   // Rotate the image context
    CGContextRotateCTM(bitmap, radians);
    // Now, draw the rotated/scaled image into the context
    CGContextScaleCTM(bitmap, 1.0, -1.0);
    CGContextDrawImage(bitmap, CGRectMake(-self.size.width / 2, -self.size.height / 2, self.size.width, self.size.height), [self CGImage]);
    UIImage *newImage = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    return newImage;
}

- (UIImage *)imageRotatedByDegrees:(CGFloat)degrees{
    return [self imageRotatedByRadians:degrees/360.f*M_PI*2];
}


#pragma mark - 处理某个特定View,只要是继承UIView的object 都可以处理,必须先import QuzrtzCore.framework
+(UIImage*)captureView:(UIView *)theView{
    //TODO: 截屏方法应该缺少scale检测，需要后期测试功能是否可用
    UIGraphicsBeginImageContextWithOptions(theView.bounds.size, YES, 0);
    CGContextRef context = UIGraphicsGetCurrentContext();
    [theView.layer renderInContext:context];
    UIImage *img = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    CGImageRef imageRef = img.CGImage;
    CGRect rect = theView.bounds;//这里可以设置想要截图的区域
    CGImageRef imageRefRect =CGImageCreateWithImageInRect(imageRef, rect);
    UIImage *sendImage = [[UIImage alloc] initWithCGImage:imageRefRect];
    return sendImage;
}
#pragma mark - size在限定范围中自适应大小
- (UIImage*)imageScaleToMaxSize:(CGSize )maxSize{
    if (CGSizeEqualToSize(CGSizeZero, maxSize)) {
        return nil;
    }
    //根据macSize，计算出适合self的最大尺寸
    BOOL referToWidthOrHeight = self.size.width/maxSize.width>=self.size.height/maxSize.height;
    float rate = referToWidthOrHeight?(maxSize.width/self.size.width):(maxSize.height/self.size.height);
    float width =  self.size.width*rate;
    float height = self.size.height*rate;
    CGSize newSize = CGSizeMake(width, height);
    // Create a graphics image context
    UIGraphicsBeginImageContext(newSize);
    // Tell the old image to draw in this new context, with the desired
    // new size
    [self drawInRect:CGRectMake(0,0,newSize.width,newSize.height)];
    // Get the new image from the context
    UIImage* newImage = UIGraphicsGetImageFromCurrentImageContext();
    //修正方向
    newImage = [newImage fixOrientationWithImageOrientation:self.imageOrientation];
    // End the context
    UIGraphicsEndImageContext();
    // Return the new image.
    return newImage;
}
- (UIImage *)fixOrientationWithImageOrientation:(UIImageOrientation )orientation {
    // No-op if the orientation is already correct
    if (orientation == UIImageOrientationUp) return self;
    // We need to calculate the proper transformation to make the image upright.
    // We do it in 2 steps: Rotate if Left/Right/Down, and then flip if Mirrored.
    CGAffineTransform transform = CGAffineTransformIdentity;
    switch (orientation) {
        case UIImageOrientationDown:
        case UIImageOrientationDownMirrored:
            transform = CGAffineTransformTranslate(transform, self.size.width, self.size.height);
            transform = CGAffineTransformRotate(transform, M_PI);
            break;
        case UIImageOrientationLeft:
        case UIImageOrientationLeftMirrored:
            transform = CGAffineTransformTranslate(transform, self.size.width, 0);
            transform = CGAffineTransformRotate(transform, M_PI_2);
            break;
            
        case UIImageOrientationRight:
        case UIImageOrientationRightMirrored:
            transform = CGAffineTransformTranslate(transform, 0, self.size.height);
            transform = CGAffineTransformRotate(transform, -M_PI_2);
            break;
        default:
            break;
    }
    switch (orientation) {
        case UIImageOrientationUpMirrored:
        case UIImageOrientationDownMirrored:
            transform = CGAffineTransformTranslate(transform, self.size.width, 0);
            transform = CGAffineTransformScale(transform, -1, 1);
            break;
        case UIImageOrientationLeftMirrored:
        case UIImageOrientationRightMirrored:
            transform = CGAffineTransformTranslate(transform, self.size.height, 0);
            transform = CGAffineTransformScale(transform, -1, 1);
            break;
        default:
            break;
    }
    // Now we draw the underlying CGImage into a new context, applying the transform
    // calculated above.
    CGContextRef ctx = CGBitmapContextCreate(NULL, self.size.width, self.size.height,
                                             CGImageGetBitsPerComponent(self.CGImage), 0,
                                             CGImageGetColorSpace(self.CGImage),
                                             CGImageGetBitmapInfo(self.CGImage));
    CGContextConcatCTM(ctx, transform);
    switch (orientation) {
        case UIImageOrientationLeft:
        case UIImageOrientationLeftMirrored:
        case UIImageOrientationRight:
        case UIImageOrientationRightMirrored:
            // Grr...
            CGContextDrawImage(ctx, CGRectMake(0,0,self.size.height,self.size.width), self.CGImage);
            break;
        default:
            CGContextDrawImage(ctx, CGRectMake(0,0,self.size.width,self.size.height), self.CGImage);
            break;
    }
    // And now we just create a new UIImage from the drawing context
    CGImageRef cgimg = CGBitmapContextCreateImage(ctx);
    UIImage *img = [UIImage imageWithCGImage:cgimg];
    CGContextRelease(ctx);
    CGImageRelease(cgimg);
    return img;
}
